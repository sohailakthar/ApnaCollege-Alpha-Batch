package M7_Functions;

public class Question4 {
    public static void main(String[] args) {
        int a = 9, b = 10, c = 3, d = -6;

        System.out.println("Min of a & d: " + Math.min(a, d));
        System.out.println("Max of b & c: " + Math.max(b, c));
        System.out.println("Sqrt of a: " + Math.sqrt(a));
        System.out.println("Power of 2^c: " + Math.pow(2, c));
        System.out.println("Abs of d: " + Math.abs(d));
    }
}
